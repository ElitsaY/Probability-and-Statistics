sim.dice.ball <- function() {
  dice <- sample(c(1:6), 1)
  if(dice == 6){
    ball <- sample(c('g','g', 'r', 'r'), 1)
  } else {
    ball <- sample(c('g', 'r', 'r', 'r', 'r'), 1)
  }
  c(dice, ball)
}

res <- replicate(10000, sim.dice.ball())
